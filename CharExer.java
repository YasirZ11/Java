
public class CharExer {

	public static void main(String[] args) {
		char ch,CH;
		
		System.out.print("Alphabets in Capital: ");
		for(CH='A';CH<='Z';CH++) {
			System.out.print(CH + ",");
		}
		System.out.println();
		System.out.print("Alphabets in simple: ");
	 for(ch='a';ch<='z';ch++) {
			System.out.print(ch + ",");
		}
	}
}
